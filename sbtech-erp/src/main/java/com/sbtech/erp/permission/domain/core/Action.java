package com.sbtech.erp.permission.domain.core;

public enum Action {

    CREATE("생성"),
    READ("조회"),
    UPDATE("수정"),
    DELETE("삭제"),

    APPROVE("승인"),
    REJECT("반려"),

    EXPORT("내보내기"),
    IMPORT("가져오기"),

    EXECUTE("실행"),
    ASSIGN("할당"),
    TRANSFER("이관"),
    ARCHIVE("보관"),
    PRINT("출력"),

    MANAGE("관리");

    private final String description;

    Action(String description) {
        this.description = description;
    }

    public String getCode() {
        return name(); // ex) "CREATE"
    }

    public String getDescription() {
        return description; // ex) "생성"
    }
}
